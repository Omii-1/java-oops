public class Operators {
  public static void main(String[] args) {
    int num1 = 12;
    int num2 = 6;
    // 1. arithmetic operators
    System.out.println(num1 + num2); // addition 18
    System.out.println(num1 - num2); // subtraction 6
    System.out.println(num1 * num2); // multiplication 72
    System.out.println(num1 / num2); // divider 2
    System.out.println(num1 % num2); // reminder 0

    // 2. assignment operator
    if(num1 != num2){
      System.out.println("hello");
    }
  }
}
